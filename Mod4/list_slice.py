monday_temperatures = [9.1, 8.8, 7.5, 6.6, 7.5]

# Check length of the list.
print(len(monday_temperatures))

# Check the fourth item from a list.
print(monday_temperatures[3])

# Check the range of items on the list
print(monday_temperatures[1:4])

# Check the items in slots 0 and 1 in a list
print(monday_temperatures[0:2])

# Alternative method to get slots 0 and 1
print(monday_temperatures[:2])

# Get the last two items in the list
print(monday_temperatures[3:5])

# Alternative option for getting the last two items.
print(monday_temperatures[3:])